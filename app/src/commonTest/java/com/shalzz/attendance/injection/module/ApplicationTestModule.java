package com.shalzz.attendance.injection.module;

import android.app.Application;
import android.content.Context;
import android.support.annotation.NonNull;

import com.google.firebase.analytics.FirebaseAnalytics;
import com.shalzz.attendance.data.local.PreferencesHelper;
import com.shalzz.attendance.data.remote.DataAPI;
import com.shalzz.attendance.injection.ApplicationContext;

import javax.inject.Named;
import javax.inject.Singleton;

import dagger.Module;
import dagger.Provides;
import okhttp3.OkHttpClient;

import static org.mockito.Mockito.mock;

/**
 * Provides application-level dependencies for an app running on a testing environment
 * This allows injecting mocks if necessary.
 */
@Module
public class ApplicationTestModule {

    private final Application mApplication;

    public ApplicationTestModule(Application application) {
        mApplication = application;
    }

    @Provides
    Application provideApplication() {
        return mApplication;
    }

    @Provides
    @ApplicationContext
    Context provideContext() {
        return mApplication;
    }

    /************* MOCKS *************/

    @Provides @Singleton @NonNull
    static OkHttpClient provideClient() {
        return mock(OkHttpClient.class);
    }

    @Provides
    @Singleton
    DataAPI provideDataAPIService() {
        return mock(DataAPI.class);
    }

    @Provides
    @Named("app")
    @Singleton
    synchronized FirebaseAnalytics provideTracker() {
        return mock(FirebaseAnalytics.class);
    }

}
